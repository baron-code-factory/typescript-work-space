const d = 2;
/**
 * 倍 ばいん で 2倍
 *
 * ### Example
 * ```js
 * console.log(double(4))
 * // => 8
 * ```
 *
 * @param value   Comment describing the `value` parameter.
 * @returns       Comment describing the return type.
 * @anotherNote   Some other value.
 */
export const double = (value: number): number => value * d;
